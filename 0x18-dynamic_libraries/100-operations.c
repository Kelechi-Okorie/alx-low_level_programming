/**
 * add - adds 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: a + b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: a x b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns modulus of a number
 * @a: first number
 * @b: second number
 *
 * Return: modulus of a number
 */
int mod(int a, int b)
{
	return (a % b);
}
