/**
 * op_add - adds two numbers
 * @a: the first number
 * @b: the second number
 *
 * Description: adds numbers @a and @b
 * Return: result of @a + @b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts two numbers
 * @a: the first number
 * @b: the second number
 *
 * Description: substracts @b from @a
 * Return: result of @a - @b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two nubers
 * @a: the first number
 * @b: the seconf numbers
 *
 * Description: multiplies @a and @b
 * Return: @a x @b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a number by another
 * @a: first number
 * @b: second number
 *
 * Description: divides @a by @b
 * Return: result of @a/@b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - does modulus arithmetic
 * @a: first number
 * @b: second number
 *
 * Description: returns @a mod @b
 * Return: result of @a % @b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
