#include <stdio.h>

/**
 * main - prints number of arguments passed to program
 * @argc: arguments count
 * @argv: arguments vector
 *
 * Description: prints number of arguments passed to program
 * Return: Always 0
 */
int main(int argc, __attribute__((unused)) char *argv[])
{
	printf("%d\n", argc);

	return (0);
}
